import cv2  # 【视觉皮层】：OpenCV，负责所有图像处理、特征寻找和画面渲染
import numpy as np  # 【数学引擎】：NumPy，负责将摄像头的底层二进制数据流转化为矩阵结构
import pyrealsense2 as rs  # 【硬件驱动】：Intel RealSense 官方 SDK，负责打通电脑与 D435 相机的物理底层
import os  # 【系统交互】：负责自动创建文件夹，防止运行到一半因为“找不到路径”而闪退

# ==============================================================================
# 模块一：全局物理约束与环境搭建
# ==============================================================================
# 【核心防坑：内角点法则】
# 物理标定板是黑白相间的方块。但 OpenCV 算法的数学逻辑只寻找“四个方块交汇的十字中心点”。
# 如果你的物理方格是 12列 x 9行，那么内角点就是 11列 x 8行。
# ⚠️ 极易报错点：OpenCV 的元组顺序必须是 (宽/列数, 高/行数)，绝不能写反！
CHECKERBOARD = (8, 11)

# 【数据金库】：相对路径设计。存放在 runs 目录下代表这是某次具体任务产生的流水数据。
SAVE_DIR = "runs/calib_data/images"
# exist_ok=True 是工业级代码的标配：文件夹如果已存在就跳过，不存在才创建，绝对不会报错卡死。
os.makedirs(SAVE_DIR, exist_ok=True)

# ==============================================================================
# 模块二：硬件底层管道初始化 (Pipeline)
# ==============================================================================
# 什么是 Pipeline？把它想象成一根连接电脑内存和相机 CMOS 传感器的高速水管。
pipeline = rs.pipeline()
config = rs.config()  # 准备向相机下发硬件配置指令

# 【分辨率锁定】：1280x720 (720P)
# 为什么不用更低的 640x480？因为标定板距离较远时，低分辨率会导致角点模糊，提取误差极大。
# 为什么不用更高的 1920x1080？因为会导致传输带宽吃紧，引发画面卡顿。1280x720 是精度与帧率的完美平衡点。
config.enable_stream(rs.stream.color, 1280, 720, rs.format.bgr8, 30)

# 正式通电，唤醒相机硬件
pipeline.start(config)

img_count = 1
print("🚀 [系统硬件就绪] 视觉流管道已打通！")
print("👉 [SOP 指南]：请拖动机械臂变换姿态。看到屏幕出现【绿色网格连线】时，按下 'S' 键触发快门。")

# ==============================================================================
# 模块三：高速实时处理主循环 (The Event Loop)
# ==============================================================================
# try...finally 结构是调用硬件驱动的最高准则！
# 如果程序中间崩溃或被用户强行关闭，finally 里的代码一定会执行，确保释放相机。
# 否则相机会被判定为“仍在被占用”，下次启动程序时直接报错，必须拔插 USB 线才能恢复。
try:
    while True:
        # 1. 【同步拉取硬件帧】
        # 等待相机的曝光和数据传输，拉取最新的一帧混合数据包
        frames = pipeline.wait_for_frames()
        color_frame = frames.get_color_frame()  # 把 RGB 彩色图像从混合包里抽离出来
        if not color_frame:
            continue  # 如果这一帧因为 USB 抖动丢包了，直接放弃，不报错，继续等下一帧

        # 2. 【数据格式转换】
        # SDK 给的是底层数据结构，必须转换成 NumPy 数组，OpenCV 才能看得懂
        img = np.asanyarray(color_frame.get_data())

        # 为什么要转灰度图？
        # 寻找角点本质上是在寻找“明暗对比最强烈的地方（梯度突变点）”。
        # 色彩对于角点提取毫无意义，反而会把计算量放大 3 倍。所以全部转成黑白灰度图运算。
        gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

        # ⚠️ 【极其致命的防呆设计】：拷贝图像
        # display_img 是用来画彩色连线给操作员看的（画花脸）。
        # img 是干干净净的原图。我们保存时必须保存原图！如果你把画了绿线的图保存去标定，算法绝对报错。
        display_img = img.copy()

        # ==============================================================================
        # 模块四：核心视觉算法 (棋盘格追踪引擎)
        # ==============================================================================
        # 【算法名称】：寻相器 (Find Chessboard Corners)
        # ret: 布尔值，找到了返回 True，没找全返回 False。
        # corners: 找到了多少个角点的粗略像素坐标。
        ret, corners = cv2.findChessboardCorners(
            gray,
            CHECKERBOARD,
            # 【工业级 Flag 组合拳】：
            # ADAPTIVE_THRESH：自适应阈值，抵抗车间局部反光和阴影的影响。
            # NORMALIZE_IMAGE：直方图均衡化，把太暗的图像自动调亮。
            # FAST_CHECK (神级参数)：先随便扫一眼，如果画面里根本没类似棋盘格的纹理，直接退出计算！
            # 如果不加这个参数，当画面里没标定板时，算法会“死磕”全图每一个像素，导致画面严重卡顿拖影！
            cv2.CALIB_CB_ADAPTIVE_THRESH + cv2.CALIB_CB_FAST_CHECK + cv2.CALIB_CB_NORMALIZE_IMAGE
        )

        if ret:
            # ---------------------------------------------------------
            # 极限精度提纯：亚像素级角点优化 (Sub-Pixel Optimization)
            # ---------------------------------------------------------
            # 普通角点提取出的坐标是整数 (比如 X:512, Y:300)。
            # 但真实的物理边缘往往落在像素与像素的缝隙之间。
            # 这个函数利用数学插值和迭代梯度下降，强行把精度算到小数点后（如 X:512.314, Y:300.871）。
            # 这就是你能让机械臂实现 1 毫米以内抓取精度的底层秘密！

            # criteria 终止条件 = (精度必须达到 0.001 像素 或者 强制迭代算满 30 次，谁先满足就停止)
            criteria = (cv2.TERM_CRITERIA_EPS + cv2.TERM_CRITERIA_MAX_ITER, 30, 0.001)
            # (11, 11) 是计算窗口大小，(-1, -1) 是死区设置（忽略中心点，防止发散）
            corners_subpix = cv2.cornerSubPix(gray, corners, (11, 11), (-1, -1), criteria)

            # 在 display_img 上绘制出彩虹色的连线，向操作员提供强烈的视觉反馈：
            # “线画全了，而且没有歪，可以按快门了！”
            cv2.drawChessboardCorners(display_img, CHECKERBOARD, corners_subpix, ret)

            # 屏幕高亮提示：目标已锁定
            cv2.putText(display_img, "TARGET LOCKED! Press 'S' to Save", (30, 50),
                        cv2.FONT_HERSHEY_SIMPLEX, 1, (0, 255, 0), 2)
        else:
            # 如果画面没有板子，或者板子被截断了，给出橙色预警，防止误按快门
            cv2.putText(display_img, "Searching for board...", (30, 50),
                        cv2.FONT_HERSHEY_SIMPLEX, 1, (0, 165, 255), 2)

        # 把画好界面的图像推送到操作员的显示器上
        cv2.imshow("Hand-Eye Collection [Industrial View]", display_img)

        # ==============================================================================
        # 模块五：终端交互与文件落盘
        # ==============================================================================
        # 每 1 毫秒扫描一次键盘状态，确保快门响应零延迟
        key = cv2.waitKey(1) & 0xFF

        if key == ord('q'):
            print("🛑 接收到手动退出指令，停止采集。")
            break

        # 【多重保险拦截】：按下 's' 键 且 必须同时满足 ret=True (识别成功) 才能触发保存！
        # 彻底杜绝手抖拍下一张废图，导致后期矩阵算不出来。
        elif key == ord('s') and ret:
            # 核心：存入硬盘的是 img (原汁原味的无污染图)
            save_path = os.path.join(SAVE_DIR, f"{img_count}.jpg")
            cv2.imwrite(save_path, img)

            print(f"✅ 咔嚓！第 {img_count} 张原片已安全落盘至：{save_path}")
            print(
                f"👉 【人工协同要求】：请立即读取机械臂示教器上的法兰坐标，新建 poses/{img_count}.txt 写入 X Y Z Rx Ry Rz！")
            img_count += 1

finally:
    # ==============================================================================
    # 模块六：安全收尾与内存清理
    # ==============================================================================
    # 无论程序是正常结束还是报错崩溃，都会执行这里的清扫工作
    pipeline.stop()  # 告诉硬件：我用完相机了，断电休眠吧
    cv2.destroyAllWindows()  # 告诉操作系统：释放窗口占用的显卡内存
    print("🔌 硬件资源已安全释放，进程结束。")